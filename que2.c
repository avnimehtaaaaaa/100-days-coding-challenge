#include <stdio.h>
int main()
{
  int first,second,sum,diff,prod,quotient;
  printf("enter first number:");
  scanf("%d",&first);
  printf("enter second number:");
  scanf("%d",&second);
  sum=first+second;
  diff=first-second;
  prod=first*second;
  quotient=first/second;
  printf("sum : %d\n",sum);
  printf("difference : %d\n",diff);
  printf("product : %d\n",prod);
  printf("quotient : %d\n",quotient);
  
  return 0;
}

  









