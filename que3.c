#include<stdio.h>
int main()
{
int length,breadth,area,perimeter;
printf("length of rectangle :");
scanf("%d",&length);
printf("breadth of rectangle :");
scanf("%d",&breadth);
area=length*breadth;
perimeter=2*(length+breadth);
printf("area :%d\n",area);
printf("perimeter :%d\n",perimeter);
return 0;
}




